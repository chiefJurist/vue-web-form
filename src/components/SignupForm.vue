<template>
    <form>
        <label>Email: </label>
        <input type="text" required v-model="email">

        <label>Password: </label>
        <input type="password" required v-model="password">

        <label>Role</label>
        <select v-model="role">
            <option value="developer">Web developer</option>
            <option value="designer">Web designer</option>
        </select>

        <!--Using two-way data binding for checkboxes-->
        <div class="terms">
            <input type="checkbox" v-model="terms" required>
            <label>Accept terms and conditions</label>
        </div>

        <!--Using arrays for multiple checboxes-->
        <div>
            <input type="checkbox" value="Shaun" v-model="names">
            <label>Shaun</label>

            <input type="checkbox" value="Yoshi" v-model="names">
            <label>Yoshi</label>

            <input type="checkbox" value="Mario" v-model="names">
            <label>Mario</label>
        </div>
    </form>
    <p>Email: {{ email }}</p>
    <p>Password: {{ password }}</p>
    <p>Role: {{ role }}</p>
    <p>Terms accepeted: {{ terms }}</p>
    <p>Names: {{ names }}</p>
</template>

<script>
    export default {
        data(){
            return{
                email: "",
                password: "",
                role: "",
                //Using a property with a boolean value to store the checkbox
                terms: false,
                //Any of the selected values will be in this array
                names: []
            }
        }
    }
</script>

